    private void ReservationSearchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReservationSearchBTNActionPerformed
//Search Button Searches the database for the trains according to the Origin and Destiantion specified.        

String StartingPoint = (String) OriginCB.getSelectedItem();
String Destination = (String) DestinationCB.getSelectedItem();
DefaultTableModel model = (DefaultTableModel) ReservationTBL.getModel();

/*
The following loop ensures that whenever a new search is made the table is first empty.
*/
for( int i = model.getRowCount() - 1; i >= 0; i-- )
{
    model.removeRow(i);
}

try{
            Class.forName("java.sql.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/ip_project","root","opjs");
            stmt = con.createStatement();
            String query = "Select * from Trains where StartingPoint = '"+StartingPoint+"' and Destination = '"+Destination+"';";
                //The program searches for the available trains form the database table "Trains".
            rs = stmt.executeQuery(query);

            while(rs.next()){
                String TrainNo = rs.getString("TrainNo");
                String TrainName = rs.getString("TrainName");
                String ArrivalTime = rs.getString("ArrivalTime");
                String DepartureTime = rs.getString("DepartureTime");
                String NOOFSEATSPERTRAIN = rs.getString("NOOFSEATSPERTRAIN");
                
            model.addRow(new Object[]{TrainNo,TrainName, StartingPoint,  DepartureTime, Destination, ArrivalTime, NOOFSEATSPERTRAIN});
            }
    stmt.close();
    con.close();
    rs.close();
}
catch(Exception e){
    JOptionPane.showMessageDialog(null, e.getMessage());
}
if(ReservationTBL.getRowCount() == 0){
    JOptionPane.showMessageDialog(null, "No Train Available for the selected combination of Starting Point and Destination.");
}
    }//GEN-LAST:event_ReservationSearchBTNActionPerformed

