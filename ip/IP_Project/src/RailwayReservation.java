    private void LoginBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBTNActionPerformed
/*
*   Login button takes the input in the form of userID and Password and matches it to the database
*   if they match then the program proceed
*   else it says wrong username or password.
*/

        try{
            Class.forName("java.sql.Driver");

            String userID = UserIDTF.getText();
            String password = new String (PasswordPF.getPassword());

            con = DriverManager.getConnection("jdbc:mysql://localhost/ip_project","root","opjs");
            stmt = con.createStatement();
            String query = "SELECT * from PASSWORD WHERE userID = '"+userID+"';";
                    //The program searches the database for the password for the entered username.
            rs = stmt.executeQuery(query);

            if(rs.next()){
                String password_stored = rs.getString("Password");
                if(password_stored.equals(password)){ 
                    //To check if the password is correct or not
                    RailwayFrame.setVisible(true);
                }
            }
            else 
                    //if the password is not correct
                JOptionPane.showMessageDialog(null, "Wrong Username or Password");
            

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_LoginBTNActionPerformed

